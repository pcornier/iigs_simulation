  path = $6
  gsub(/^"/, "", path); gsub(/"$/, "", path)
  paths[key] = (paths[key] ? paths[key] SUBSEP path : path)
}
END {
  ord["BOOTED"]=0; ord["TEXT_ONLY"]=1; ord["BLANK"]=2
  ord["TIMEOUT"]=3; ord["CRASH"]=4
  n = 0
  for (k in count) keys[++n] = k
  for (i=1; i<=n; i++) for (j=i+1; j<=n; j++) {
    oi = ord[status[keys[i]]]; oj = ord[status[keys[j]]]
    if (oi > oj || (oi == oj && count[keys[i]] < count[keys[j]])) {
